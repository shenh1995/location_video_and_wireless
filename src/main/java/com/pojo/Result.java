package com.pojo;

public class Result {

	public String mac_name;
	public String picture_paths;
	public int status;

	public String getMac_name() {
		return mac_name;
	}

	public void setMac_name(String mac_name) {
		this.mac_name = mac_name;
	}

	public String getPicture_paths() {
		return picture_paths;
	}

	public void setPicture_paths(String picture_paths) {
		this.picture_paths = picture_paths;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
